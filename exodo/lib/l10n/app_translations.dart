/// Punto de entrada principal: devuelve el mapa de traducciones para un código.
/// Si el código no existe, cae a español (es).
Map<String, String> translationsFor(String code) {
  switch (code) {
    case 'en':
    case 'en_GB':
      return _en;
    case 'fr':
      return _fr;
    case 'ht':
      return _ht;
    case 'pt':
      return _pt;
    case 'pt_BR':
      return _ptBR;
    case 'it':
      return _it;
    case 'de':
      return _de;
    case 'ru':
      return _ru;
    case 'zh':
      return _zh;
    case 'ja':
      return _ja;
    case 'ar':
      return _ar;
    case 'ko':
      return _ko;
    case 'hi':
      return _hi;
    case 'es':
    default:
      return _es;
  }
}

